void hsl_to_rgb_jet(double h, double s, double l, double rgb[3]) /* color conversion from HSL to RGB */
/* h = hue, s = saturation, l = luminosity */
{
    double c = 0.0, m = 0.0, x = 0.0;

    c = (1.0 - fabs(2.0 * l - 1.0)) * s;
    m = 1.0 * (l - 0.5 * c);
    x = c * (1.0 - fabs(fmod(h / 60.0, 2) - 1.0));

    if (h >= 0.0 && h < 60.0)
    {
        rgb[0] = c + m;
        rgb[1] = x + m;
        rgb[2] = m;
    }
    else if (h < 120.0)
    {
        rgb[0] = x + m;
        rgb[1] = c + m;
        rgb[2] = m;
    }
    else if (h < 180.0)
    {
        rgb[0] = m;
        rgb[1] = c + m;
        rgb[2] = x + m;
    }
    else if (h < 240.0)
    {
        rgb[0] = m;
        rgb[1] = x + m;
        rgb[2] = c + m;
    }
    else if (h < 300.0)
    {
        rgb[0] = x + m;
        rgb[1] = m;
        rgb[2] = c + m;
    }
    else if (h < 360.0)
    {
        rgb[0] = c + m;
        rgb[1] = m;
        rgb[2] = x + m;
    }
    else
    {
        rgb[0] = m;
        rgb[1] = m;
        rgb[2] = m;
    }
}

void hsl_to_rgb(double h, double s, double l, double rgb[3]) /* color conversion from HSL to RGB */
{
    int color_hue, i;
    double r, g, b, ratio = 0.711111111, interpolate; /* ratio equals 256/360 */

    switch (COLOR_PALETTE)
    {
    case (COL_JET):
    {
        hsl_to_rgb_jet(h, s, l, rgb);
        break;
    }
    case (COL_HSLUV):
    {
        hsluv2rgb(h, 100.0 * s, l, &r, &g, &b);
        rgb[0] = 2.0 * l * r * 100.0;
        rgb[1] = 2.0 * l * g * 100.0;
        rgb[2] = 2.0 * l * b * 100.0;
        break;
    }
    case (COL_GRAY):
    {
        color_hue = h / 360.0;
        rgb[0] = color_hue;
        rgb[1] = color_hue;
        rgb[2] = color_hue;
        break;
    }
    case (COL_TURBO):
    {
        color_hue = 255 - (int)(ratio * h);
        for (i = 0; i < 3; i++)
            rgb[i] = 2.0 * l * (double)turbo_srgb_floats[color_hue][i];
        break;
    }
    case (COL_VIRIDIS):
    {
        color_hue = 255 - (int)(ratio * h);
        for (i = 0; i < 3; i++)
            rgb[i] = 2.0 * l * (double)viridis_srgb_floats[color_hue][i];
        break;
    }
    case (COL_MAGMA):
    {
        color_hue = 255 - (int)(ratio * h);
        for (i = 0; i < 3; i++)
            rgb[i] = 2.0 * l * (double)magma_srgb_floats[color_hue][i];
        break;
    }
    case (COL_INFERNO):
    {
        color_hue = 255 - (int)(ratio * h);
        for (i = 0; i < 3; i++)
            rgb[i] = 2.0 * l * (double)inferno_srgb_floats[color_hue][i];
        break;
    }
    case (COL_PLASMA):
    {
        color_hue = 255 - (int)(ratio * h);
        for (i = 0; i < 3; i++)
            rgb[i] = 2.0 * l * (double)plasma_srgb_floats[color_hue][i];
        break;
    }
    case (COL_CIVIDIS):
    {
        color_hue = 255 - (int)(ratio * h);
        for (i = 0; i < 3; i++)
            rgb[i] = 2.0 * l * (double)cividis_srgb_floats[color_hue][i];
        break;
    }
    case (COL_PARULA):
    {
        color_hue = 255 - (int)(ratio * h);
        for (i = 0; i < 3; i++)
            rgb[i] = 2.0 * l * (double)parula_srgb_floats[color_hue][i];
        break;
    }
    case (COL_TWILIGHT):
    {
        color_hue = 510 - (int)(2.0 * ratio * h);
        if (color_hue > 510)
            color_hue = 510;
        else if (color_hue < 0)
            color_hue = 0;
        for (i = 0; i < 3; i++)
            rgb[i] = 2.0 * l * (double)twilight_srgb_floats[color_hue][i];
        break;
    }
    case (COL_TWILIGHT_SHIFTED):
    {
        //             color_hue = 255 - (int)(2.0*ratio*h);
        color_hue = (int)(2.0 * ratio * h) - 255;
        if (color_hue > 510)
            color_hue -= 510;
        else if (color_hue < 0)
            color_hue += 510;
        for (i = 0; i < 3; i++)
            rgb[i] = 2.0 * l * (double)twilight_srgb_floats[color_hue][i];
        break;
    }
    case (COL_TURBO_CYCLIC):
    {
        if (h < 330.0)
        {
            color_hue = 255 - (int)(ratio * h * 12.0 / 11.0);
            for (i = 0; i < 3; i++)
                rgb[i] = 2.0 * l * (double)turbo_srgb_floats[color_hue][i];
        }
        else /* use a convex combination to interpolate between extremal Turbo values */
        {
            if (h > 360.0)
                h = 360.0;
            interpolate = (h - 330.0) / 30.0;
            for (i = 0; i < 3; i++)
                rgb[i] = (1.0 - interpolate) * (double)turbo_srgb_floats[255][i] + interpolate * (double)turbo_srgb_floats[0][i];
        }
        break;
    }
    }
}

